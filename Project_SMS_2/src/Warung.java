    private void bubayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubayActionPerformed
        // TODO add your handling code here:
        long total = ksr.totalPembayaran();

        if (total <= 0) {
            System.out.println("Upss seperti nya kamu harus mengisi orderan terlebih dahulu");
            showMessageDialog("Upss seperti nya kamu harus mengisi orderan terlebih dahulu");
            return;
        }

        long totalBayar = Long.parseLong(totubay.getText());
        // ada kondisi jika total uang pembeli lebih besar dari total pembayaran
        if (totalBayar >= total) {
            // mengubah text field total kembalian, dengan memanggil fungsi totalKembalian()
            totukem.setText(Long.toString(ksr.totalKembalian(totalBayar)));
            System.out.println("UANG CUKUP");
            showMessageDialog("Berhasil Melakukan Pembayaran");
        } else {
            System.out.println("UANG TIDAK CUKUP");
            showMessageDialog("Upss uang tidak cukup");
        }
    }//GEN-LAST:event_bubayActionPerformed

