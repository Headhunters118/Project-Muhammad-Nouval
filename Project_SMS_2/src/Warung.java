    private void tbelimakananKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbelimakananKeyReleased
        // TODO add your handling code here:
        String text = tbelimakanan.getText();

        // ada sebuah kondisi
        if (text.isEmpty() || Integer.parseInt(text) <= 0) {
            System.out.println("MINIMAL BELI LEBIH DARI 1 !!");
            // menonaktifkan button hitung total makanan nya
            bhitungtotmakanan.setEnabled(false);
            return;
        }
        System.out.println("Total Beli : " + text);

        // jika text pada var belimakanan nya tidak kosong
        // mengkonversi totalbeli nya menjadi integer
        int totalBeli = Integer.parseInt(text);
        // memanggil fungsi setJumlahBeliMakanan() dengan isi paramater nya yaitu totalBeli
        ksr.setJumlahBeliMakanan(totalBeli);
        
        // button total hitung makanan
        bhitungtotmakanan.setEnabled(true);
        
        // selanjut nya kita ke fungsi aksi button total
    }//GEN-LAST:event_tbelimakananKeyReleased

